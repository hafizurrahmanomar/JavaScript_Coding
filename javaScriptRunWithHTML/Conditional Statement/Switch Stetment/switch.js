//Switch Statment

let dayName = `Saturday`;

switch (dayName) {
  case `Sunday`:
    console.log(`Today Name: Sunday`);
    break;

  case `Monday`:
    console.log(`Today Name: Monday`);
    break;

  case `Tuesday`:
    console.log(`Today Name: Tuesday`);
    break;

  case `Wednesday`:
    console.log(`Today Name: Wednesday`);
    break;

  case `Thuresday`:
    console.log(`Today Name: Thuresday`);
    break;

  case `Friday`:
    console.log(`Today Name: Friday`);
    break;

  case `Saturday`:
    console.log(`Today Name: Saturday`);
    break;

 default:
 console.log(`Today Name: Not Valid`);
}