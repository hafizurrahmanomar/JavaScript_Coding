//Continue Statement

for (let i =0; i<=10; i++) {
    if(i===6 || i===9){

        continue;
    }
    else{
        console.log(i);
    }
}
    
console.log("Break statement start")

//break statement
for(let i = 0; i<=10; i++) {
    if(i===6){
        break;
    }
    else{
        console.log(i);
    }
}

