//Number String Auto Conversion without Addition

let number = 15;
let numberString = '10';
//concatenation
let result = number + numberString;
console.log(result);
//obstruction
 result = number -numberString;
 console.log(result);
//multiplication
result = number *numberString;
console.log(result);
//division
result = number / numberString;
console.log(result);
//modulus

result = number % numberString;
console.log(result);
//power
result = number ** numberString;
console.log(result);

